public class Operators {
    public static void main(String[] args){
        int a=-8,b=2,res;
        //left shift
        int ls=a<<b;
        System.out.println("Left shift of "+a+" is "+ls);

        // Right shift
        res=a>>b;
        System.out.println("Right shift of "+a+" is  "+res);




    }
}
